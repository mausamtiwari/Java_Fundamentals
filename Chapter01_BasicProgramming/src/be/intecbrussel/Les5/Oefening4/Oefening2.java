// Gebruik een while loop. Maak een programma dat de getallen van 50 tot en met 100 omgekeerd afdrukken.

package be.intecbrussel.Les5.Oefening4;

public class Oefening2 {
    public static void main(String[] args) {
        int number = 100;
        while (number >= 50) {
            System.out.println(number);
            number--;
        }
    }
}
