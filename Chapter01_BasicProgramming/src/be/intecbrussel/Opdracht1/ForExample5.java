package be.intecbrussel.Opdracht1;

public class ForExample5 {
    public static void main(String[] args) {
        int i;
        for (i = -10; i <= 10; i++) {
            if (i > 0) {
                System.out.println("+ " + i);
            }
             else{
                System.out.println(i);
            }

        }
    }
}